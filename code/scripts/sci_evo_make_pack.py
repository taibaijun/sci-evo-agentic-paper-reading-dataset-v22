from __future__ import annotations

import argparse
import json
import shutil
import sys
from pathlib import Path

sys.path.insert(0, str(Path(__file__).resolve().parents[1]))

from sci_evo_pipeline.schema import load_cases_from_dir, write_dataset_jsonl


def main() -> None:
    parser = argparse.ArgumentParser(description="Create a review/submission draft package.")
    parser.add_argument("--run-root", default=r"outputs\deepseek_trial")
    parser.add_argument("--pack-dir", default=r"outputs\submission_draft")
    parser.add_argument("--sample-count", type=int, default=10)
    args = parser.parse_args()

    run_root = Path(args.run_root)
    pack_dir = Path(args.pack_dir)
    if pack_dir.exists():
        shutil.rmtree(pack_dir)
    pack_dir.mkdir(parents=True)

    cases = load_cases_from_dir(run_root / "cases")
    cases.sort(key=lambda c: c.get("quality_control", {}).get("auto_quality_score", 0), reverse=True)
    write_dataset_jsonl(pack_dir / "dataset.jsonl", cases)
    (pack_dir / "samples_10.json").write_text(
        json.dumps(cases[: args.sample_count], ensure_ascii=False, indent=2),
        encoding="utf-8",
    )
    for name in ["quality_report.json"]:
        src = run_root / name
        if src.exists():
            shutil.copy2(src, pack_dir / name)
    readme = f"""# Sci-Evo Dataset Draft

This package is generated by the local Sci-Evo extraction pipeline.

- dataset_type: Sci-Evo
- case_count: {len(cases)}
- sample_file: samples_10.json
- full_file: dataset.jsonl
- source: open-access papers parsed with MinerU
- human_reviewed: false for auto-generated draft fields unless manually edited

Manual review should focus on evidence quotes, quantitative metrics, and any case with low evidence coverage.
"""
    (pack_dir / "README.md").write_text(readme, encoding="utf-8")
    print(f"pack_dir={pack_dir} cases={len(cases)}")


if __name__ == "__main__":
    main()
